    private void btnSalvarVeiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarVeiculoActionPerformed
        // TODO add your handling code here:
        String placa;
        String marca;
        String modelo;
        String cor;
        String ano;
        Veiculo veiculoDado;
        
        placa = txtPlacaVeiculo.getText();
        marca = txtMarcaVeiculo.getText();
        modelo = txtModeloVeiculo.getText();
        cor = txtCorVeiculo.getText();
        ano = txtAnoVeiculo.getText();
        
        int intAno = Integer.parseInt(ano);
        
        veiculoDado = new Veiculo(placa, marca, modelo, cor, intAno);
        veiculoVector.add(veiculoDado);
        
        JOptionPane.showMessageDialog(null, "Veiculo salvo com sucesso!", "Mensagem", JOptionPane.INFORMATION_MESSAGE);
        carregarTabelaVeiculo();
        txtPlacaVeiculo.setText("");
        txtMarcaVeiculo.setText("");
        txtModeloVeiculo.setText("");
        txtCorVeiculo.setText("");
        txtAnoVeiculo.setText("");
    }//GEN-LAST:event_btnSalvarVeiculoActionPerformed

