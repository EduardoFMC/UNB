    public void carregarTabelaVeiculo(){
        DefaultTableModel modeloV = new DefaultTableModel(new Object[] {"Placa","Marca","Modelo","Cor","Ano"},0);
        for(int i = 0; i < veiculoVector.size();i++){
            Object linha[] = new Object[]{veiculoVector.get(i).getPlaca(), veiculoVector.get(i).getMarca(), 
                veiculoVector.get(i).getModelo(), veiculoVector.get(i).getCor(), veiculoVector.get(i).getAno()};
            modeloV.addRow(linha);
        }
        tblVeiculos.setModel(modeloV);
    }

