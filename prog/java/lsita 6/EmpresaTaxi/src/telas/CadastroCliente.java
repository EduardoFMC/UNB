    private void btnSalvarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarClienteActionPerformed
        String id;
        String RG;
        String CPF;
        String nome;
        String telefone;
        int intId;
        Cliente clienteDado;
        
        id = txtId.getText();
        intId = Integer.parseInt(id);
        
        RG = txtRg.getText();
        int intRG = Integer.parseInt(RG);
        
        CPF = txtCpf.getText();
        int intCPF = Integer.parseInt(CPF);
        
        nome = txtNome.getText();
        
        telefone = txtTelefone.getText();
        int intTelefone = Integer.parseInt(telefone);
        clienteDado = new Cliente(intId, RG, CPF, nome, telefone);
        clienteVector.add(clienteDado);
        
        JOptionPane.showMessageDialog(null, "Cliente salvo com sucesso!", "Mensagem", JOptionPane.INFORMATION_MESSAGE);
        carregarTabelaCliente();
        txtCpf.setText("");
        txtId.setText("");
        txtNome.setText("");
        txtRg.setText("");
        txtTelefone.setText("");
        
    }//GEN-LAST:event_btnSalvarClienteActionPerformed

