    public void carregarTabelaCliente(){
        DefaultTableModel modeloC = new DefaultTableModel(new Object[] {"ID","RD","CPF","Nome","Telefone"},0);
        for(int i = 0; i < clienteVector.size();i++){
            Object linha[] = new Object[]{clienteVector.get(i).getId(), clienteVector.get(i).getRG(), clienteVector.get(i).getCPF(), clienteVector.get(i).getNome(), clienteVector.get(i).getTelefone()};
            modeloC.addRow(linha);
        }
        tblClientes.setModel(modeloC);
    
    }

