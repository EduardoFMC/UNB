    private void btnSalvarMotoristaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarMotoristaActionPerformed
        // TODO add your handling code here:
        String id;
        String nome;
        String telefone;
        String CNH;
        int intId;
        Motorista motoristaDado;
        
        id = txtIdMotorista.getText();
        intId = Integer.parseInt(id);
        
        CNH = txtCnhMotorista.getText();
        nome = txtNomeMotorista.getText();
        telefone = txtTelefoneMotorista.getText();
        
        motoristaDado = new Motorista(intId, nome, telefone, CNH);
        motoristaVector.add(motoristaDado);
        
        JOptionPane.showMessageDialog(null, "Motorista salvo com sucesso!", "Mensagem", JOptionPane.INFORMATION_MESSAGE);
        carregarTabelaMotorista();
        txtCnhMotorista.setText("");
        txtIdMotorista.setText("");
        txtNomeMotorista.setText("");
        txtTelefoneMotorista.setText("");
    }//GEN-LAST:event_btnSalvarMotoristaActionPerformed

