    public void carregarTabelaMotorista(){
        DefaultTableModel modeloM = new DefaultTableModel(new Object[] {"ID","Nome","Telefone","CNH"},0);
        for(int i = 0; i < motoristaVector.size();i++){
            Object linha[] = new Object[]{motoristaVector.get(i).getId(), 
                            motoristaVector.get(i).getNome(), motoristaVector.get(i).getTelefone(), motoristaVector.get(i).getCNH()};
            modeloM.addRow(linha);
        }
        tblMotorista.setModel(modeloM);
    
    }

